import mongoose from "mongoose";

const productSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    discount: {
      type: Number,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    size: {
      type: String,
      required: true,
    },
    color: {
      type: String,
      required: true,
    },
    images: [
      {
        url: { type: String, required: true },
      },
    ],
  },
  { timestamps: true }
);

const productModel =
  mongoose.models.products || mongoose.model("products", productSchema);

export default productModel;
